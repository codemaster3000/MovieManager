package blackmediamanager.medialibrary.xrelinfo.jsondata.media;

/**
 *
 * @author fabian
 */
public class XRcovers {
    private String type;
    private String description;
    private int time;
    private String url_full;
    private String url_thumb;
    private String youtube_id;
    private String video_url;

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the time
     */
    public int getTime() {
        return time;
    }

    /**
     * @param time the time to set
     */
    public void setTime(int time) {
        this.time = time;
    }

    /**
     * @return the url_full
     */
    public String getUrl_full() {
        return url_full;
    }

    /**
     * @param url_full the url_full to set
     */
    public void setUrl_full(String url_full) {
        this.url_full = url_full;
    }

    /**
     * @return the url_thumb
     */
    public String getUrl_thumb() {
        return url_thumb;
    }

    /**
     * @param url_thumb the url_thumb to set
     */
    public void setUrl_thumb(String url_thumb) {
        this.url_thumb = url_thumb;
    }

    /**
     * @return the youtube_id
     */
    public String getYoutube_id() {
        return youtube_id;
    }

    /**
     * @param youtube_id the youtube_id to set
     */
    public void setYoutube_id(String youtube_id) {
        this.youtube_id = youtube_id;
    }

    /**
     * @return the video_url
     */
    public String getVideo_url() {
        return video_url;
    }

    /**
     * @param video_url the video_url to set
     */
    public void setVideo_url(String video_url) {
        this.video_url = video_url;
    }
}
